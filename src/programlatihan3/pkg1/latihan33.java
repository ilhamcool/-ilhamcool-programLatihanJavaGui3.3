    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        if (edit.getText().equals("Edit")){
            edit.setText("Edit");
            edit.setMnemonic('E');
            tambah.setText("Simpan");
            tambah.setMnemonic('B');
            exit.setEnabled(false);
            nim.setEnabled(true);
            nama.setEnabled(true);
            kelas.setEnabled(true);
            nim.requestFocus();
            
        } else {
            JOptionPane.showMessageDialog(null, "Mengupdate Data Nim '" + nim.getText() + "' nama '" + nama.getText()+ "' kelas '" + kelas.getText() );
            edit.setText("Simpan");
            edit.setMnemonic('S');
            tambah.setText("Simpan");
            edit.setMnemonic('S');
            exit.setEnabled(true);
            nim.setEnabled(false);
            nama.setEnabled(false);
            kelas.setEnabled(false);
            edit.requestFocus();
    }//GEN-LAST:event_editActionPerformed
    }
