    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        if (tambah.getText().equals("Tambah")){
            tambah.setText("Simpan");
            tambah.setMnemonic('S');
            edit.setText("Batal");
            edit.setMnemonic('B');
            exit.setEnabled(false);
            nim.setEnabled(true);
            nama.setEnabled(true);
            kelas.setEnabled(true);
            nim.requestFocus();
            
        } else {
            JOptionPane.showMessageDialog(null, "Menyimpan Data Nim '" + nim.getText() + "' nama '" + nama.getText()+ "' kelas '" + kelas.getText() );
            tambah.setText("Tambah");
            tambah.setMnemonic('T');
            edit.setText("Edit");
            edit.setMnemonic('E');
            exit.setEnabled(true);
            nim.setEnabled(false);
            nama.setEnabled(false);
            kelas.setEnabled(false);
            tambah.requestFocus();
            
        }
    }//GEN-LAST:event_tambahActionPerformed

